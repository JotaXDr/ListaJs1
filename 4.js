// Faça um script que recebe um comando ('start', 'stop', 'pause')
// e executa uma ação correspondente usando switch.


var comando = prompt("Insira um comando ('start', 'stop', 'pause')")

switch (comando){
  case 1:
  case comando = 'start':
    alert("Iniciando programa")
  case 2:
  case comando = 'stop':
    alert("Programa pausado")
  case 3:
  case comando = 'pause':
    alert("Programa encerrado.")
    break
}
